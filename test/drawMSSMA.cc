double GetCrossPointX(double *xs, double *ys,int n, double ytmp, bool increasing){
	if(n<2) {
		cout<<"GetCrossPointX: Error: n should be >= 2, otherwise no interpolation"<<endl;
		exit(0);
	}
	// suppose to be sorted from small to large. 
	int nless=-1, nmore=-1;
	for(int i=0; i<n; i++)	{
		if(increasing){
			if(ys[i]>=ytmp && nmore<0) nmore=i;
			if(ys[i]<=ytmp) nless=i;
		}else{
			if(ys[i]>=ytmp) nmore=i;
			if(ys[i]<=ytmp && nless<0) nless=i;
		}
	}
	if(nmore==nless) return xs[nmore];
	if(nmore<0) {
		//return -999.; //FIXME
		if(increasing){
			nmore=n-1;
			nless=n-2;
		}else{
			nmore=0;
			nless=1;
		}
	}
	if(nless<0) {
		//return -999.; //FIXME
		if(increasing){
			nmore=1;
			nless=0;
		}else{
			nmore=n-2;
			nless=n-1;
		}
	}
	double x=LinearInterpolation(xs[nless], ys[nless], xs[nmore], ys[nmore], ytmp);
	//	cout<<"x1="<<xs[nless]<<" y1="<<ys[nless]<<" x2="<<xs[nmore]<<" y2="<<ys[nmore]<<" ytmp="<<ytmp<<" --> xtmp="<<x<<endl;
	return x;
}
